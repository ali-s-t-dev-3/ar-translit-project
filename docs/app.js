export function transliterate(text, transliterationRules = rules) {
  let result = "";
  let index = 0;

  while (index < text.length) {
    const three = text.slice(index, index + 3);
    const two = text.slice(index, index + 2);
    const one = text.slice(index, index + 1);

    if (three in transliterationRules) {
      result += transliterationRules[three];
      index += 3;
    } else if (two in transliterationRules) {
      result += transliterationRules[two];
      index += 2;
    } else if (one in transliterationRules) {
      result += transliterationRules[one];
      index += 1;
    } else {
      result += text[index];
      index += 1;
    }
  }

  return result;
}

export function dictionarySearch(prefix, wordList = words) {
  return wordList.filter((word) => word.startsWith(prefix));
}

export function frequencyRanking(suggestions) {
  return [...suggestions].sort((left, right) => {
    const rightScore = frequency[right] ?? 0;
    const leftScore = frequency[left] ?? 0;
    return rightScore - leftScore;
  });
}

export function getTopSuggestions(arabicText, limit = 3) {
  return frequencyRanking(dictionarySearch(arabicText)).slice(0, limit);
}

export function getPrediction(arabicText) {
  const nextWords = bigrams[arabicText];

  if (!nextWords) {
    return null;
  }

  return Object.entries(nextWords).sort(([, leftScore], [, rightScore]) => rightScore - leftScore)[0][0];
}

export function validateInput(userInput) {
  if (userInput.trim() === "") {
    return "Please enter a transliterated word.";
  }

  const normalizedInput = userInput.toLowerCase();
  const allowed = new Set("abcdefghijklmnopqrstuvwxyz23679 ");

  for (const char of normalizedInput) {
    if (!allowed.has(char)) {
      return "Invalid character detected.";
    }
  }

  for (let index = 0; index < normalizedInput.length; index += 1) {
    if (
      normalizedInput.slice(index, index + 3) in rules ||
      normalizedInput.slice(index, index + 2) in rules ||
      normalizedInput.slice(index, index + 1) in rules
    ) {
      return null;
    }
  }

  return "Input not recognised.";
}

function setText(id, value) {
  const node = document.getElementById(id);
  if (node) {
    node.textContent = value;
  }
}

function renderSuggestions(suggestions) {
  const list = document.getElementById("suggestions");
  if (!list) {
    return;
  }

  list.innerHTML = "";

  if (suggestions.length === 0) {
    const empty = document.createElement("li");
    empty.className = "empty-state";
    empty.textContent = "No dictionary match yet";
    list.append(empty);
    return;
  }

  for (const suggestion of suggestions) {
    const item = document.createElement("li");
    item.textContent = suggestion;
    item.dir = "rtl";
    list.append(item);
  }
}

function updatePreview(input) {
  const error = validateInput(input.value);
  const hasError = Boolean(error);

  setText("error", error ?? "");
  document.body.classList.toggle("has-error", hasError);

  if (hasError) {
    setText("arabic-output", "");
    setText("prediction", "No prediction yet");
    setText("count", "0 chars");
    renderSuggestions([]);
    return;
  }

  const arabicText = transliterate(input.value);
  const suggestions = getTopSuggestions(arabicText);
  const prediction = getPrediction(arabicText);

  setText("arabic-output", arabicText);
  setText("prediction", prediction ?? "No prediction yet");
  setText("count", `${input.value.length} chars`);
  renderSuggestions(suggestions);
}

function initDemo() {
  const input = document.getElementById("latin-input");
  const exampleList = document.getElementById("examples");

  if (!input || !exampleList) {
    return;
  }

  input.addEventListener("input", () => updatePreview(input));

  for (const example of exampleInputs) {
    const button = document.createElement("button");
    button.type = "button";
    button.textContent = example;
    button.addEventListener("click", () => {
      input.value = example;
      input.focus();
      updatePreview(input);
    });
    exampleList.append(button);
  }

  updatePreview(input);
}

if (typeof document !== "undefined") {
  initDemo();
}
